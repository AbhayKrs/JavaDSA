package com.abhay.javabasics;

public class Basics_01_Literals {
    public static void main(String[] args) {
        int a = 10;
        float b = 10.4f;
        double c = 10.23;
        long d = 12L;
        char e = 'a';
        boolean f = false;
        System.out.println("Literals: " + a + ", " + b + ", " + c + ", " + d + ", " + e + ", " + f + ", " );
    }
}
